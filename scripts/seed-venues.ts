async function seed() {
  console.log(`Seeding ${SEED_VENUES.length} venues...`)

  for (const v of SEED_VENUES) {
    const { error } = await supabase.from('venues').upsert(
      {
        name: v.name,
        slug: v.slug,
        type: v.type,
        lat: v.lat,
        lng: v.lng,
        sun_query_point: `SRID=4326;POINT(${v.patio_lng} ${v.patio_lat})`,
        patio_confidence: 'estimated',
        website: v.website ?? null,
        rating: v.rating,
        price_level: v.price_level,
        hours: v.hours,
        patio_season_only: true,
        address: v.address,
        neighborhood: v.neighborhood,
        data_sources: { seed: true },
      },
      { onConflict: 'slug' }
    )

    if (error) {
      console.error(`Error seeding ${v.name}:`, error)
    } else {
      console.log(`  ✓ ${v.name}`)
    }
  }

  console.log('Done!')
}

seed().catch(console.error)
